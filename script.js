export function getCategories(items) {
  return [...new Set(items.map((item) => item.category))];
}

export function filterResources(items, { query = "", category = "All" } = {}) {
  const normalizedQuery = query.trim().toLowerCase();

  return items.filter((item) => {
    const matchesCategory = category === "All" || item.category === category;
    if (!matchesCategory) return false;
    if (!normalizedQuery) return true;

    const searchableText = [
      item.title,
      item.description,
      item.category,
      item.audience,
      ...item.tags
    ].join(" ").toLowerCase();

    return searchableText.includes(normalizedQuery);
  });
}

function createCategoryButton(category, activeCategory) {
  const button = document.createElement("button");
  button.type = "button";
  button.className = "filter-button";
  button.dataset.category = category;
  button.textContent = category;
  button.setAttribute("aria-pressed", String(category === activeCategory));
  return button;
}

function createResourceCard(resource) {
  const article = document.createElement("article");
  article.className = "resource-card";

  const meta = document.createElement("div");
  meta.className = "resource-meta";
  meta.innerHTML = `<span>${resource.category}</span><span>${resource.audience}</span>`;

  const title = document.createElement("h2");
  const link = document.createElement("a");
  link.href = resource.url;
  link.target = "_blank";
  link.rel = "noreferrer";
  link.textContent = resource.title;
  title.append(link);

  const description = document.createElement("p");
  description.textContent = resource.description;

  const tags = document.createElement("div");
  tags.className = "tag-row";
  resource.tags.slice(0, 4).forEach((tag) => {
    const tagEl = document.createElement("span");
    tagEl.textContent = tag;
    tags.append(tagEl);
  });

  article.append(meta, title, description, tags);
  return article;
}

function init() {
  const searchInput = document.querySelector("#search");
  const categoryFilters = document.querySelector("#categoryFilters");
  const resourceGrid = document.querySelector("#resourceGrid");
  const resultCount = document.querySelector("#resultCount");

  if (!searchInput || !categoryFilters || !resourceGrid || !resultCount) return;

  let activeCategory = "All";
  const categories = ["All", ...getCategories(resources)];

  function render() {
    categoryFilters.replaceChildren(
      ...categories.map((category) => createCategoryButton(category, activeCategory))
    );

    const filtered = filterResources(resources, {
      query: searchInput.value,
      category: activeCategory
    });

    resultCount.textContent = `${filtered.length} resource${filtered.length === 1 ? "" : "s"} shown`;

    if (filtered.length === 0) {
      const empty = document.createElement("div");
      empty.className = "empty-state";
      empty.textContent = "No matching resources found.";
      resourceGrid.replaceChildren(empty);
      return;
    }

    resourceGrid.replaceChildren(...filtered.map(createResourceCard));
  }

  categoryFilters.addEventListener("click", (event) => {
    const button = event.target.closest("button[data-category]");
    if (!button) return;
    activeCategory = button.dataset.category;
    render();
  });

  searchInput.addEventListener("input", render);
  render();
}

if (typeof document !== "undefined") {
  init();
}
